// NOTE: Ported from Google Cloud GenAI/Imagen to Cloudflare Workers
// - Removed Genkit dependency in favor of Cloudflare-native interfaces
// - Replaced Google AI API calls with generic image generation interface
// - Using Cloudflare R2 for storing generated images
// - Using Cloudflare D1 for tracking generation metadata
// - Using Cloudflare Queues for async image processing

export interface Env {
  // R2 bucket for storing generated images
  IMAGES_BUCKET: R2Bucket;
  
  // D1 database for tracking generation metadata
  DB: D1Database;
  
  // KV namespace for caching model configurations
  MODEL_CACHE: KVNamespace;
  
  // Queue for async image processing
  IMAGE_QUEUE: Queue;
  
  // API keys for image generation services (stored as secrets)
  IMAGE_API_KEY?: string;
  
  // Optional: Cloudflare Access JWT verification
  // JWT_SECRET?: string;
}

/**
 * Image generation configuration schema
 */
export interface ImagenConfig {
  /** Override the API key provided at plugin initialization */
  apiKey?: string;
  
  /** The number of images to generate, from 1 to 4 (inclusive). Default is 1 */
  numberOfImages?: number;
  
  /** Desired aspect ratio of the output image */
  aspectRatio?: '1:1' | '9:16' | '16:9' | '3:4' | '4:3';
  
  /** Control if/how images of people will be generated by the model */
  personGeneration?: 'dont_allow' | 'allow_adult' | 'allow_all';
  
  /** Image generation service to use */
  service?: 'stability' | 'openai' | 'anthropic' | 'custom';
  
  /** Model name/version for the selected service */
  model?: string;
  
  /** Quality setting (service-specific) */
  quality?: 'standard' | 'hd';
  
  /** Style guidance (service-specific) */
  style?: 'natural' | 'vivid' | string;
  
  /** Seed for reproducible results */
  seed?: number;
}

/**
 * Media part containing generated image
 */
export interface MediaPart {
  media: {
    /** URL to the generated image (R2 public URL or data URL) */
    url: string;
    /** MIME type of the image */
    contentType: string;
    /** Optional: R2 object key if stored in bucket */
    objectKey?: string;
    /** Optional: Generation ID for tracking */
    generationId?: string;
  };
}

/**
 * Image generation request
 */
export interface GenerateRequest {
  /** Text prompt for image generation */
  prompt: string;
  
  /** Optional base image for variations/edits (base64 encoded) */
  baseImage?: string;
  
  /** Optional mask image for inpainting (base64 encoded) */
  maskImage?: string;
  
  /** Configuration options */
  config?: ImagenConfig;
  
  /** Optional user ID for tracking/rate limiting */
  userId?: string;
  
  /** Optional request metadata */
  metadata?: Record<string, any>;
}

/**
 * Image generation response
 */
export interface GenerateResponse {
  /** Generated images */
  images: MediaPart[];
  
  /** Generation ID for tracking */
  generationId: string;
  
  /** Processing time in milliseconds */
  processingTime: number;
  
  /** Model/service used */
  model: string;
  
  /** Service used for generation */
  service: string;
  
  /** Optional usage/cost information */
  usage?: {
    /** Number of images generated */
    imageCount: number;
    /** Credits/tokens used */
    credits?: number;
    /** Estimated cost */
    cost?: number;
  };
  
  /** Optional error information */
  error?: {
    code: string;
    message: string;
    details?: any;
  };
}

/**
 * Image generation service interface
 */
export interface ImageGenerationService {
  /** Service name */
  name: string;
  
  /** Generate images from prompt */
  generate(
    request: GenerateRequest,
    env: Env,
    ctx: ExecutionContext
  ): Promise<GenerateResponse>;
  
  /** Check if service is available/configured */
  isAvailable(env: Env): boolean;
}

/**
 * Stability AI image generation service
 */
export class StabilityAIService implements ImageGenerationService {
  name = 'stability';
  
  isAvailable(env: Env): boolean {
    return !!env.IMAGE_API_KEY;
  }
  
  async generate(
    request: GenerateRequest,
    env: Env,
    ctx: ExecutionContext
  ): Promise<GenerateResponse> {
    const apiKey = request.config?.apiKey || env.IMAGE_API_KEY;
    if (!apiKey) {
      throw new Error('Stability AI API key not configured');
    }
    
    const startTime = Date.now();
    const generationId = crypto.randomUUID();
    
    // Prepare request for Stability AI API
    const stabilityRequest = {
      text_prompts: [{ text: request.prompt }],
      cfg_scale: 7,
      height: this.getHeight(request.config?.aspectRatio),
      width: this.getWidth(request.config?.aspectRatio),
      samples: request.config?.numberOfImages || 1,
      steps: 30,
      style_preset: request.config?.style || 'enhance',
    };
    
    // Make API call
    const response = await fetch('https://api.stability.ai/v1/generation/stable-diffusion-xl-1024-v1-0/text-to-image', {
      method: 'POST',
      headers: {
        'Authorization': `Bearer ${apiKey}`,
        'Content-Type': 'application/json',
        'Accept': 'application/json',
      },
      body: JSON.stringify(stabilityRequest),
    });
    
    if (!response.ok) {
      const error = await response.text();
      throw new Error(`Stability AI API error: ${response.status} ${error}`);
    }
    
    const result = await response.json();
    
    // Store images in R2 and create media parts
    const images: MediaPart[] = [];
    
    for (const artifact of result.artifacts) {
      const objectKey = `generations/${generationId}/${crypto.randomUUID()}.png`;
      
      // Store in R2
      await env.IMAGES_BUCKET.put(objectKey, this.base64ToArrayBuffer(artifact.base64), {
        httpMetadata: {
          contentType: 'image/png',
        },
        customMetadata: {
          prompt: request.prompt.substring(0, 100),
          userId: request.userId || 'anonymous',
          generationId,
          service: this.name,
          model: request.config?.model || 'stable-diffusion-xl-1024-v1-0',
        },
      });
      
      // Create public URL (or signed URL for private buckets)
      const publicUrl = `/images/${objectKey}`;
      
      images.push({
        media: {
          url: publicUrl,
          contentType: 'image/png',
          objectKey,
          generationId,
        },
      });
    }
    
    // Store generation metadata in D1
    await this.storeGenerationMetadata(request, generationId, images, env, startTime);
    
    return {
      images,
      generationId,
      processingTime: Date.now() - startTime,
      model: request.config?.model || 'stable-diffusion-xl-1024-v1-0',
      service: this.name,
      usage: {
        imageCount: images.length,
      },
    };
  }
  
  private getHeight(aspectRatio?: string): number {
    switch (aspectRatio) {
      case '9:16': return 1024;
      case '16:9': return 576;
      case '3:4': return 896;
      case '4:3': return 768;
      case '1:1':
      default: return 1024;
    }
  }
  
  private getWidth(aspectRatio?: string): number {
    switch (aspectRatio) {
      case '9:16': return 576;
      case '16:9': return 1024;
      case '3:4': return 768;
      case '4:3': return 896;
      case '1:1':
      default: return 1024;
    }
  }
  
  private base64ToArrayBuffer(base64: string): ArrayBuffer {
    const binaryString = atob(base64);
    const bytes = new Uint8Array(binaryString.length);
    for (let i = 0; i < binaryString.length; i++) {
      bytes[i] = binaryString.charCodeAt(i);
    }
    return bytes.buffer;
  }
  
  private async storeGenerationMetadata(
    request: GenerateRequest,
    generationId: string,
    images: MediaPart[],
    env: Env,
    startTime: number
  ): Promise<void> {
    await env.DB.prepare(`
      INSERT INTO image_generations (
        id, user_id, prompt, service, model, 
        aspect_ratio, image_count, processing_time,
        created_at, metadata
      ) VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?)
    `).bind(
      generationId,
      request.userId || 'anonymous',
      request.prompt,
      this.name,
      request.config?.model || 'stable-diffusion-xl-1024-v1-0',
      request.config?.aspectRatio || '1:1',
      images.length,
      Date.now() - startTime,
      Date.now(),
      JSON.stringify({
        config: request.config,
        metadata: request.metadata,
        image_keys: images.map(img => img.media.objectKey),
      })
    ).run();
  }
}

