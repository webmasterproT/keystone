/**
 * Main image generation service registry
 */
export class ImageGenerationServiceRegistry {
  private services: Map<string, ImageGenerationService> = new Map();
  
  constructor() {
    this.register(new StabilityAIService());
    this.register(new OpenAIService());
  }
  
  register(service: ImageGenerationService): void {
    this.services.set(service.name, service);
  }
  
  getService(name: string): ImageGenerationService | undefined {
    return this.services.get(name);
  }
  
  getAvailableServices(env: Env): string[] {
    return Array.from(this.services.values())
      .filter(service => service.isAvailable(env))
      .map(service => service.name);
  }
  
  getDefaultService(env: Env): string {
    const available = this.getAvailableServices(env);
    return available[0] || 'stability';
  }
}

/**
 * Image generation model reference
 */
export class ImageGenerationModel {
  constructor(
    public name: string,
    public service: string,
    public config: ImagenConfig = {}
  ) {}
  
  /**
   * Generate images using this model
   */
  async generate(
    request: GenerateRequest,
    env: Env,
    ctx: ExecutionContext
  ): Promise<GenerateResponse> {
    const registry = new ImageGenerationServiceRegistry();
    const service = registry.getService(this.service);
    
    if (!service) {
      throw new Error(`Image generation service '${this.service}' not found`);
    }
    
    if (!service.isAvailable(env)) {
      throw new Error(`Image generation service '${this.service}' not available`);
    }
    
    // Merge model config with request config
    const mergedConfig: ImagenConfig = {
      ...this.config,
      ...request.config,
      service: this.service,
      model: this.name,
    };
    
    const requestWithConfig: GenerateRequest = {
      ...request,
      config: mergedConfig,
    };
    
    return service.generate(requestWithConfig, env, ctx);
  }
}

