/**
 * Get an image generation model by name
 */
export function getModel(name: string): ImageGenerationModel {
  const model = IMAGE_MODELS[name as keyof typeof IMAGE_MODELS];
  if (!model) {
    throw new Error(`Image generation model '${name}' not found`);
  }
  return model;
}

/**
 * List available image generation models
 */
export function listModels(env: Env): string[] {
  const registry = new ImageGenerationServiceRegistry();
  const availableServices = registry.getAvailableServices(env);
  
  return Object.keys(IMAGE_MODELS).filter(modelName => {
    const model = IMAGE_MODELS[modelName as keyof typeof IMAGE_MODELS];
    return availableServices.includes(model.service);
  });
}

/**
 * Cloudflare Worker handler for image generation
 */
export async function handleImageGeneration(
  request: Request,
  env: Env,
  ctx: ExecutionContext
): Promise<Response> {
  try {
    // Parse request
    const body = await request.json() as GenerateRequest;
    
    // Validate request
    if (!body.prompt || typeof body.prompt !== 'string') {
      return new Response(
        JSON.stringify({ error: 'Prompt is required and must be a string' }),
        { status: 400, headers: { 'Content-Type': 'application/json' } }
      );
    }
    
    // Get model (default to first available)
    const modelName = body.config?.model || listModels(env)[0];
    if (!modelName) {
      return new Response(
        JSON.stringify({ error: 'No image generation models available' }),
        { status: 503, headers: { 'Content-Type': 'application/json' } }
      );
    }
    
    const model = getModel(modelName);
    
    // Generate images
    const response = await model.generate(body, env, ctx);
    
    return new Response(
      JSON.stringify(response),
      { 
        status: 200, 
        headers: { 
          'Content-Type': 'application/json',
          'Cache-Control': 'no-store',
        } 
      }
    );
    
  } catch (error) {
    console.error('Image generation error:', error);
    
    return new Response(
      JSON.stringify({ 
        error: 'Image generation failed',
        details: error instanceof Error ? error.message : 'Unknown error'
      }),
      { 
        status: 500, 
        headers: { 'Content-Type': 'application/json' } 
      }
    );
  }
}

/**
 * Async image generation via Cloudflare Queue
 */
export async function handleAsyncImageGeneration(
  request: Request,
  env: Env,
  ctx: ExecutionContext
): Promise<Response> {
  try {
    const body = await request.json() as GenerateRequest;
    
    // Validate request
    if (!body.prompt || typeof body.prompt !== 'string') {
      return new Response(
        JSON.stringify({ error: 'Prompt is required and must be a string' }),
        { status: 400, headers: { 'Content-Type': 'application/json' } }
      );
    }
    
    // Generate job ID
    const jobId = crypto.randomUUID();
    
    // Send to queue for async processing
    await env.IMAGE_QUEUE.send({
      jobId,
      ...body,
      timestamp: Date.now(),
    });
    
    return new Response(
      JSON.stringify({
        jobId,
        status: 'queued',
        message: 'Image generation job submitted for async processing',
        checkStatusUrl: `/jobs/${jobId}`,
      }),
      { 
        status: 202, 
        headers: { 'Content-Type': 'application/json' } 
      }
    );
    
  } catch (error) {
    console.error('Async image generation error:', error);
    
    return new Response(
      JSON.stringify({ 
        error: 'Failed to submit image generation job',
        details: error instanceof Error ? error.message : 'Unknown error'
      }),
      { 
        status: 500, 
        headers: { 'Content-Type': 'application/json' } 
      }
    );
  }
}

/**
 * Queue consumer for async image generation
 */
export async function handleImageGenerationQueue(
  batch: MessageBatch<any>,
  env: Env,
  ctx: ExecutionContext
): Promise<void> {
  for (const message of batch.messages) {
    try {
      const job = message.body as GenerateRequest & { jobId: string };
      
      // Get model
      const modelName = job.config?.model || listModels(env)[0];
      if (!modelName) {
        throw new Error('No image generation models available');
      }
      
      const model = getModel(modelName);
      
      // Generate images
      const response = await model.generate(job, env, ctx);
      
      // Store result in KV for retrieval
      await env.MODEL_CACHE.put(
        `job:${job.jobId}`,
        JSON.stringify({
          status: 'completed',
          result: response,
          completedAt: Date.now(),
        }),
        { expirationTtl: 86400 } // 24 hours
      );
      
      message.ack();
      
    } catch (error) {
      console.error('Queue processing error:', error);
      
      // Store error in KV
      const job = message.body as GenerateRequest & { jobId: string };
      await env.MODEL_CACHE.put(
        `job:${job.jobId}`,
        JSON.stringify({
          status: 'failed',
          error: error instanceof Error ? error.message : 'Unknown error',
          failedAt: Date.now(),
        }),
        { expirationTtl: 86400 }
      );
      
      message.retry();
    }
  }
}

/**
 * Check job status
 */
export async function handleJobStatus(
  request: Request,
  env: Env,
  ctx: ExecutionContext
): Promise<Response> {
  const url = new URL(request.url);
  const jobId = url.pathname.split('/').pop();
  
  if (!jobId) {
    return new Response(
      JSON.stringify({ error: 'Job ID is required' }),
      { status: 400, headers: { 'Content-Type': 'application/json' } }
    );
  }
  
  const jobData = await env.MODEL_CACHE.get(`job:${jobId}`);
  
  if (!jobData) {
    return new Response(
      JSON.stringify({ 
        jobId,
        status: 'not_found',
        message: 'Job not found or expired'
      }),
      { status: 404, headers: { 'Content-Type': 'application/json' } }
    );
  }
  
  return new Response(
    jobData,
    { 
      status: 200, 
      headers: { 'Content-Type': 'application/json' } 
    }
  );
}

