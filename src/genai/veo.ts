/**
 * Check if a model name is a valid Veo model name
 */
export function isVeoModelName(value?: string): value is VeoModelName {
  return !!value?.startsWith('veo-');
}

/**
 * Extract text from a generate request
 */
export function extractText(request: GenerateRequest<unknown>): string {
  if (typeof request.prompt === 'string') {
    return request.prompt;
  }
  // NOTE: Simplified from original Google implementation
  // In a real implementation, you would extract from message content
  return '';
}

