export interface VeoConfig {
  /** Desired aspect ratio of the output video. */
  aspectRatio?: '9:16' | '16:9';
  /** Control if/how images of people will be generated by the model. */
  personGeneration?: 'dont_allow' | 'allow_adult' | 'allow_all';
  /** Length of each output video in seconds, between 5 and 8. */
  durationSeconds?: number;
  /** Enable or disable the prompt rewriter. Enabled by default. */
  enhancePrompt?: boolean;
  /** Negative prompt to avoid certain content */
  negativePrompt?: string;
  /** Additional configuration options */
  [key: string]: unknown;
}

