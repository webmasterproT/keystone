/**
 * Extract version from model reference
 */
export function extractVersion(ref: ModelReference<unknown>): string {
  const match = ref.name.match(/veo-([\d.]+)/);
  return match ? match[1] : 'latest';
}

/**
 * Check and normalize model name
 */
export function checkModelName(version: string): string {
  if (version.startsWith('veo-')) {
    return version;
  }
  return `veo-${version}`;
}

/**
 * Get model name from full model string
 */
export function modelName(fullName: string): string {
  const parts = fullName.split('/');
  return parts[parts.length - 1];
}

/**
 * Convert Veo parameters for API request
 */
export function toVeoParameters(
  request: GenerateRequest<VeoConfig>
): VeoParameters {
  const out: VeoParameters = {
    ...request?.config,
  };

  // Remove null values
  for (const k in out) {
    if (out[k as keyof VeoParameters] === null) {
      delete out[k as keyof VeoParameters];
    }
  }

  // Remove fields not part of API parameters
  delete (out as any).apiKey;
  delete (out as any).version;

  return out;
}

/**
 * Convert Veo API operation to internal operation format
 */
export function fromVeoOperation(
  apiOp: VeoOperation
): Operation<GenerateResponseData> {
  const res: Operation<GenerateResponseData> = { 
    id: apiOp.name 
  };
  
  if (apiOp.done !== undefined) {
    res.done = apiOp.done;
  }

  if (apiOp.error) {
    res.error = { message: apiOp.error.message };
  }

  if (
    apiOp.response &&
    apiOp.response.generateVideoResponse &&
    apiOp.response.generateVideoResponse.generatedSamples
  ) {
    res.output = {
      finishReason: 'stop',
      raw: apiOp.response,
      message: {
        role: 'model',
        content: apiOp.response.generateVideoResponse.generatedSamples.map(
          (s) => {
            return {
              media: {
                url: s.video.uri,
              },
            };
          }
        ),
      },
    };
  }

  return res;
}

/**
 * Create a model reference for Veo models
 */
export function model(
  version: string,
  config: VeoConfig = {}
): ModelReference<VeoConfig> {
  const name = checkModelName(version);
  return {
    name: `cloudflare/${name}`,
    config,
    info: {
      supports: {
        media: true,
        multiturn: false,
        tools: false,
        systemRole: false,
        output: ['media'],
        longRunning: true,
      },
    },
  };
}

