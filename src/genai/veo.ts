/**
 * Cloudflare Worker handler for Veo video generation
 */
export default {
  async fetch(request: Request, env: Env, ctx: ExecutionContext): Promise<Response> {
    const url = new URL(request.url);
    
    // Handle CORS preflight
    if (request.method === 'OPTIONS') {
      return new Response(null, {
        headers: {
          'Access-Control-Allow-Origin': '*',
          'Access-Control-Allow-Methods': 'GET, POST, OPTIONS',
          'Access-Control-Allow-Headers': 'Content-Type, Authorization',
        },
      });
    }
    
    // Initialize database schema if needed
    if (env.DB && url.pathname === '/init') {
      await initDatabase(env.DB);
      return new Response('Database initialized', { status: 200 });
    }
    
    // List available models
    if (url.pathname === '/models' && request.method === 'GET') {
      const models = listActions();
      return Response.json(models, {
        headers: { 'Access-Control-Allow-Origin': '*' },
      });
    }
    
    // Start video generation
    if (url.pathname === '/generate' && request.method === 'POST') {
      try {
        const body = await request.json() as GenerateRequest<VeoConfig>;
        const modelName = body.config?.version || 'veo';
        
        const veoModel = defineModel(env, modelName);
        const operation = await veoModel.start(body);
        
        return Response.json(operation, {
          headers: { 'Access-Control-Allow-Origin': '*' },
          status: 202, // Accepted (long-running operation)
        });
      } catch (error) {
        return Response.json(
          { error: error instanceof Error ? error.message : 'Unknown error' },
          { 
            status: 400,
            headers: { 'Access-Control-Allow-Origin': '*' },
          }
        );
      }
    }
    
    // Check operation status
    if (url.pathname.startsWith('/operations/') && request.method === 'GET') {
      const operationId = url.pathname.split('/')[2];
      const modelName = url.searchParams.get('model') || 'veo';
      
      const veoModel = defineModel(env, modelName);
      const operation = await veoModel.check({ id: operationId });
      
      return Response.json(operation, {
        headers: { 'Access-Control-Allow-Origin': '*' },
      });
    }
    
    return new Response('Not Found', { status: 404 });
  },
};

/**
 * Initialize database schema for Veo operations
 */
async function initDatabase(db: D1Database): Promise<void> {
  await db.exec(`
    CREATE TABLE IF NOT EXISTS veo_operations (
      id TEXT PRIMARY KEY,
      status TEXT NOT NULL,
      request TEXT,
      result TEXT,
      error TEXT,
      created_at INTEGER,
      completed_at INTEGER
    );
    
    CREATE INDEX IF NOT EXISTS idx_veo_operations_status ON veo_operations(status);
    CREATE INDEX IF NOT EXISTS idx_veo_operations_created ON veo_operations(created_at);
  `);
}